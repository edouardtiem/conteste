export function DepartementsVoisins({
  currentDeptCode,
  currentType,
}: DepartementsVoisinsProps) {
  const neighborCodes = ADJACENCY[currentDeptCode] ?? [];

  const neighbors = neighborCodes
    .map((code) => {
      const dept = getDepartementByCode(code);
      if (!dept) return null;
      return dept;
    })
    .filter((d): d is NonNullable<typeof d> => d !== null)
    .slice(0, 6);

  if (neighbors.length === 0) return null;

  return (
    <section className="mb-8">
      <h2 className="text-h2 text-gris-titre mb-4">Départements voisins</h2>
      <div className="grid grid-cols-1 sm:grid-cols-2 md:grid-cols-3 gap-3">
        {neighbors.map((dept) => (
          <Link
            key={dept.code}
            href={`/guides/${currentType}/${dept.code}`}
            className="bg-white border border-gris-bordure hover:border-bleu-france rounded-card p-4 transition-colors group"
          >
            <span className="text-[14px] font-bold text-bleu-france group-hover:underline">
              {dept.code} — {dept.nom}
            </span>
            <span className="block text-[13px] text-gris-mention mt-1">
              {dept.tribunal}
            </span>
          </Link>
        ))}
      </div>
    </section>
  );
}
